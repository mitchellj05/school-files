.mode column


-- create tables
CREATE TABLE tutor_groups(
  tg_code      VARCHAR(5) NOT NULL,
  tutor        VARCHAR(15) NOT NULL,
  room         VARCHAR(5) NOT NULL,
  meeting_day  VARCHAR(9) NOT NULL,
  meeting_time TIME NOT NULL,

  PRIMARY KEY (tg_code)
);


CREATE TABLE students(
  student_no INTEGER NOT NULL,
  forename   VARCHAR(20) NOT NULL,
  surname    VARCHAR(30) NOT NULL,
  address    VARCHAR(30) NOT NULL,
  town       VARCHAR(15) NOT NULL,
  postcode   VARCHAR(8) NOT NULL,
  tg_code    VARCHAR(5) NOT NULL,

  CHECK (student_no >= 100000 AND student_no <= 199999),

  PRIMARY KEY (student_no),
  FOREIGN KEY (tg_code) REFERENCES tutor_groups(tg_code)
);


CREATE TABLE assessments(
  test_code VARCHAR(6) NOT NULL,
  title     VARCHAR(30) NOT NULL,
  test_type VARCHAR(30) NOT NULL,
  duration  INTEGER NOT NULL,
  writer    VARCHAR(15) NOT NULL,
  pass_mark INTEGER NOT NULL,

  CHECK (duration >= 15 AND duration <= 150),
  CHECK (pass_mark >= 0 AND pass_mark <= 100),

  PRIMARY KEY (test_code)
);


CREATE TABLE results(
  student_no INTEGER NOT NULL,
  test_code  VARCHAR(6) NOT NULL,
  mark       INTEGER NOT NULL,
  test_date  DATE NOT NULL,

  CHECK (mark >= 0 AND mark <= 100),
  CHECK (test_date >= '2013-08-15' AND test_date <= '2014-05-12'),

  PRIMARY KEY (student_no, test_code),
  FOREIGN KEY (student_no) REFERENCES students(student_no),
  FOREIGN KEY (test_code) REFERENCES assessments(test_code)
);


